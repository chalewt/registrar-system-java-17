    private void cmbFacultyItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbFacultyItemStateChanged
        if (cmbFaculty.getSelectedIndex() < 0) {
            return;
        }
        try {
            ComboBoxItem facultyItem = (ComboBoxItem) cmbFaculty.getSelectedItem();
            int facultyCode = facultyItem.getCodeValue();
            ComboBoxItem.loadDepartment(cmbDepartment, facultyCode);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error occurred:\n" + ex.getMessage());
        }
    }//GEN-LAST:event_cmbFacultyItemStateChanged

    private void btnPhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPhotoActionPerformed
        JFileChooser phtChsr = new JFileChooser();
        phtChsr.setDialogTitle("Attach Photo");
        phtChsr.setCurrentDirectory(new File(System.getProperty("user.home")));
        phtChsr.setFileSelectionMode(JFileChooser.FILES_ONLY);
        //set choosable file type
        phtChsr.addChoosableFileFilter(new FileNameExtensionFilter("Image Files", "jpg", "png", "gif", "bmp", "jpeg"));
        phtChsr.setAcceptAllFileFilterUsed(false);
        int rslt = phtChsr.showOpenDialog(this);
        if (rslt == JFileChooser.APPROVE_OPTION) {
            File photo = phtChsr.getSelectedFile();
            fileName = photo.getAbsolutePath();
            txtPhoto.setText(fileName);
            ImageIcon iconPhoto = new ImageIcon(fileName);
            lblPhoto.setText("");
            lblPhoto.setIcon(iconPhoto);
        }
    }//GEN-LAST:event_btnPhotoActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (cmbDepartment.getSelectedIndex() > -1 && !txtID.getText().isEmpty() && !txtName.getText().isEmpty() && !txtFatherName.getText().isEmpty() && cmbSex.getSelectedIndex() > -1) {
            try {
                Employee employee = new Employee();

                employee.setEmployeeId(txtID.getText().toUpperCase());
                employee.setName(txtName.getText().toUpperCase());
                employee.setFatherName(txtFatherName.getText().toUpperCase());
                employee.setGrandfatherName(txtGrandName.getText().toUpperCase());
                employee.setSex(cmbSex.getSelectedItem().toString().toUpperCase());
                //
                if (dtpDateBirth.getDate() != null) {
                    employee.setDateOfBirth(dtpDateBirth.getDate());
                } else {
                    employee.setDateOfBirth(null);
                }
                if (dtpHiredDate.getDate() != null) {
                    employee.setHiredDate(dtpHiredDate.getDate());
                } else {
                    employee.setHiredDate(null);
                }
                //
                employee.setPhone(txtPhone.getText());
                employee.setEmail(txtEmail.getText());
                employee.setPhoto(fileName);
                //
                ComboBoxItem deptItem = (ComboBoxItem) cmbDepartment.getSelectedItem();
                int deptCode = deptItem.getCodeValue();
                //
                boolean saved = employee.registerEmployee(employee, deptCode);
                if (saved) {
                    JOptionPane.showMessageDialog(this, "Employee Registration completed successfully.", "Registrar Information System", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Employee Registration failed. Try again", "Registrar Information System", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(this, "Employee ID already exist. ");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error occurred.\n" + e.getMessage());
            } finally {

            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill all the required fields.", "Registrar Information System", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocusInWindow();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

