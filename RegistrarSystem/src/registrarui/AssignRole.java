    private void LoadRole(String username) {
        try {
            cmbRole.removeAllItems();
            Role r = new Role();
            ArrayList<Role> rList = new ArrayList<Role>();
            rList = r.getUnAssignedRoleList(username);
            for (Role roleInList : rList) {
                ComboBoxItem roleItem = new ComboBoxItem(roleInList.getId(), roleInList.getRole() + " - " + roleInList.getDescription());
                cmbRole.addItem(roleItem);
            }
        } catch (Exception ex) {

        }
    }
