    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if ("".equals(txtUsername.getText()) || cmbRole.getSelectedIndex()<0) {
            JOptionPane.showMessageDialog(this, "Fill the required information.", "Registrar Information System", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            ComboBoxItem item = (ComboBoxItem) cmbRole.getSelectedItem();
            int roleId = item.getCodeValue();
            Role r = new Role();
            int statu = 0;
            if (chkStatus.isSelected()) {
                statu = 1;
            }
            int assigned = r.assignRole(txtUsername.getText().toLowerCase(), roleId, statu, dtpEffectiveDate.getDate(), dtpExpiryDate.getDate());
            if (assigned == 1) {
                JOptionPane.showMessageDialog(this, "Role Assignment Completed Successfully.", "Registrar Information System", JOptionPane.INFORMATION_MESSAGE);
                 LoadRole(txtUsername.getText().toLowerCase());
            }
            else if(assigned == -1) {
                JOptionPane.showMessageDialog(this, "The user already has a role.", "Registrar Information System", JOptionPane.ERROR_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(this, "Role Assignment failed. Try again", "Registrar Information System", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error occurred.\n" + ex.getMessage(), "Registrar Information System", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (!"".equals(txtId.getText()) || !"".equals(txtUsername.getText())) {
            try {
                Account accData = new Account();
                accData = accData.getUserAccount(txtId.getText().toUpperCase(), txtUsername.getText().toLowerCase());
                if (accData != null) {
                    txtId.setText(accData.getEmployeeId());
                    txtUsername.setText(accData.getUsername());
                    txtFullName.setText(accData.getName() + " " + accData.getFatherName());
                    LoadRole(accData.getUsername());
                } else {
                    JOptionPane.showMessageDialog(this, "Record not found.", "Registrar Information System", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error occurred.\n" + ex.getMessage(), "Registrar Information System", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please, Enter Employee ID or Username to search.", "Registrar Information System", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

