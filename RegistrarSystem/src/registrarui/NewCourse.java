    private void cmbDepartmentItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDepartmentItemStateChanged
        if(cmbDepartment.getSelectedIndex()<0){
            return;
        }
        try {
            ComboBoxItem deptItem = (ComboBoxItem) cmbDepartment.getSelectedItem();
            int deptCode = deptItem.getCodeValue();
            ComboBoxItem.loadCurriculum(cmbCurriculum, deptCode);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error occurred:\n" + ex.getMessage());
        }
    }//GEN-LAST:event_cmbDepartmentItemStateChanged

