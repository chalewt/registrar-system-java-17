    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        //Call registerCourse method
        if(cmbCurriculum.getSelectedIndex() < 0 || txtCode.getText()=="" || txtTitle.getText() ==""){
            JOptionPane.showMessageDialog(this, "Please fill all the required fields");
            return;
        }
        try{
            //Read input
            //create course instatnce 
            Course course = new Course();
            course.setCode(txtCode.getText());
            course.setTitle(txtTitle.getText());
            course.setCredit(Integer.parseInt(txtCredit.getText()));
            course.setLecture(Integer.parseInt(txtLecture.getText()));
            course.setLabratory(Integer.parseInt(txtLaboratory.getText()));
            course.setTutor(Integer.parseInt(txtTutor.getText()));
            course.setHomeStudy(Integer.parseInt(txtHomeStudy.getText()));
            course.setDeliveryYear(Integer.parseInt(txtYear.getText()));
            course.setDeliverySemester(Integer.parseInt(txtSemester.getText()));
            //
            course.setCatagory(cmbCatagory.getSelectedItem().toString());
            course.setOfferOption(cmbOffer.getSelectedItem().toString());
            //
            ComboBoxItem currItem = (ComboBoxItem) cmbCurriculum.getSelectedItem();
            int currCode = currItem.getCodeValue();
            course.setCurrCode(currCode);
            //
            ComboBoxItem ownerDeptItem = (ComboBoxItem) cmbOwnerDepartment.getSelectedItem();
            int ownerDeptCode = ownerDeptItem.getCodeValue();
            course.setOwnerDept(ownerDeptCode);
            
            boolean saved = course.registerCourse(course);
            if(saved == true){
                JOptionPane.showMessageDialog(this, "Course information saved successfully.");
            }
            else{
                JOptionPane.showMessageDialog(this, "Saving course data failed. Try again.");
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Error occurred.\n" + ex.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

