private void getFacultyList() {
        String sqlSelectFaculty = "Select Faculty_Code, Faculty_Name From Faculty order by Faculty_Name ASC";
        try {
            Connection conn = DbConnection.getConn();
            Statement stmt = conn.createStatement();
            ResultSet rstFacultyList = stmt.executeQuery(sqlSelectFaculty);
            while (rstFacultyList.next()) {
                int facultyCode = rstFacultyList.getInt("Faculty_Code");
                String facultyName = rstFacultyList.getString("Faculty_Name");
                //Combine faculty code and name and add to combo box item
                ComboBoxItem itemFaculty = new ComboBoxItem(facultyCode, facultyName);
                cmbFaculty.addItem(itemFaculty);
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Error occurred:\n" + ex.getMessage());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database server error.\n" + e.getMessage(), "Registrar Information System", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error occurred:\n" + ex.getMessage());
        }
    }
