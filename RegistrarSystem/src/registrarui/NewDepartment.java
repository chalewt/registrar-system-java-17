    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if ("".equals(txtName.getText()) || cmbFaculty.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(this, "Please, fill all the required fields.", "Registrar Information System", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //Call the method
        try {
            Department dept = new Department();
            dept.setName(txtName.getText());
            dept.setPhone(txtPhone.getText());
            dept.setFax(txtFax.getText());
            dept.setEmail(txtEmail.getText());
            ComboBoxItem faultyItem = (ComboBoxItem) cmbFaculty.getSelectedItem();
            int facultyCode = faultyItem.getCodeValue();
            boolean saved = dept.registerDepartment(dept, facultyCode);
            if (saved = true) {
                JOptionPane.showMessageDialog(this, "Record saved successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Saving record failed. Try Agian.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error occurred.\n" + ex.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

